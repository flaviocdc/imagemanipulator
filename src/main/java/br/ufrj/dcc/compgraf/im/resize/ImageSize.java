package br.ufrj.dcc.compgraf.im.resize;

public class ImageSize {

  private int width;
  private int height;
  
  public ImageSize(int width, int height)
  {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return width;
  }

  public int getHeight() {
    return height;
  }
}